#include <stdio.h>

/**
 * op_add - Adder
 * @a: First num
 * @b: Second num
 * Return: Sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Diff
 * @a: First num
 * @b: Second num
 * Return: Diff
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Product
 * @a: First num
 * @b: Second num
 * Return: Prod
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Div
 * @a: First num
 * @b: Second num
 * Return: Div
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Mod
 * @a: First num
 * @b: Second num
 * Return: Mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
